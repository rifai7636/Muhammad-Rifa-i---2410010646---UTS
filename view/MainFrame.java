    /**
     * Mengupdate SEMUA komponen UI (List dan Table)
     */
    private void updateUI() {
        updateKategoriList();
        
        // Otomatis pilih kategori pertama jika ada
        if (!kategoriListModel.isEmpty()) {
            jListKategori.setSelectedIndex(0);
        } else {
            // Jika tidak ada kategori, kosongkan tabel
            barangTableModel.setRowCount(0);
        }
        clearFields();
    }
    
    /**
     * Memperbarui daftar Kategori (JList)
     */
    private void updateKategoriList() {
        kategoriListModel.clear(); // Kosongkan list
        List<String> daftarKategori = barangService.getDaftarKategori();
        for (String kategori : daftarKategori) {
            kategoriListModel.addElement(kategori);
        }
    }
    
    /**
     * Memperbarui daftar Barang (JTable) berdasarkan kategori yang dipilih
     */
    private void updateBarangTable(String kategori) {
        if (kategori == null) {
            barangTableModel.setRowCount(0);
            return; 
        }
        
        List<Barang> daftarBarang = barangService.getBarangByKategori(kategori);
        TableUtil.updateBarangTable(barangTableModel, daftarBarang);
    }
    
